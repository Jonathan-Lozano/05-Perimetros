public class Perimetros {

	/**
	 * 
	 * @param lado
	 * @return Metodo para calcular el perimetro de un cuadrado Recibe el valor
	 *         del lado del cuadrado y devuelve el perimetro
	 */
	public int cuadrado(int lado) {
		return lado + lado + lado + lado;
	}

}
